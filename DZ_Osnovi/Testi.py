from main import Power

def run_tests():
    # Тест 1: Проверка для положительной степени
    assert Power(2, 3) == 8.0

    # Тест 2: Проверка для отрицательной степени
    assert Power(3, -2) == 1 / 9.0

    # Тест 3: Проверка для нулевой степени
    assert Power(5, 0) == 1.0

    # Тест 4: Проверка для четной степени
    assert Power(4, 2) == 16.0

    # Тест 5: Проверка для дробной степени
    assert Power(2, 0.5) == 1.4142135623730951  # приблизительное значение корня из 2

    # Тест 6: Проверка для дробной отрицательной степени
    assert Power(9, -0.5) == 0.3333333333333333  # приблизительное значение обратного корня из 9

    print("Все тесты пройдены успешно!")

# Запустим тесты
run_tests()
